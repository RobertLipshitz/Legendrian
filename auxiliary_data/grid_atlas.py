"""
XO grid-diagram representatives for Legendrian knots.

Source
------
https://sites.math.duke.edu/~ng/atlas/XO%20Permutation.tex
(Lenny Ng's Legendrian knot atlas)

Conventions
-----------
GRID_ATLAS[name] is a list of (X_perm, O_perm) pairs, **0-indexed**.
  X_perm[j] = row of the X marker in column j
  O_perm[j] = row of the O marker in column j
Rows are 0-indexed from the bottom; columns 0-indexed from the left.
Vertical strands pass over horizontal strands at each crossing.
The Legendrian front projection is obtained by rotating the grid 45° CCW.

The source file uses 1-indexed permutations; all values here have been
decremented by 1 to match the convention expected by Leg((X_perm, O_perm)).

Naming
------
K3_1   = 3_1  (right-handed trefoil, Rolfsen table)
mK3_1  = mirror of 3_1
K4_1   = 4_1 = m(4_1)  (self-mirror; stored once under K4_1)
K6_3   = 6_3 = m(6_3)  (self-mirror; stored once under K6_3)
K8_19  = 8_19 (subscript becomes underscore), etc.
K11n38 = 11n38  (Hoste-Thistlethwaite non-alternating notation)

Multiple representatives are stored as a list; access via
  Leg('K5_2.0')  →  Leg(GRID_ATLAS['K5_2'][0])
  Leg('K5_2.1')  →  Leg(GRID_ATLAS['K5_2'][1])

Some entries (marked with * in the source) have representatives of
different grid sizes within the same knot type; these are included as
separate list entries.
"""

